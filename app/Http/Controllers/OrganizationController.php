    {
        $orgs=DB::table('organizations')->where('gh_account_id',$id)->get();
        // dd($orgs);
        $gh_profs=array();
        foreach ($orgs as $org){
            $gh_profs[]=DB::table('gh_profiles')->where('id',$org->organization_id)->first();
        }
        // dd($gh_profs);

        return view("organization",["gh_profs"=>$gh_profs,"id"=>$id]);
    }

